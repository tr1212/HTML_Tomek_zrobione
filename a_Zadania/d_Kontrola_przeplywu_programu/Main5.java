
public class Main5 {

	public static void main(String[] args) {
		int resultFor = 0;
		int resultWhile = 0;
		for (resultFor = 1; resultFor <= 10; resultFor = resultFor + 1) {
			System.out.print(resultFor);
		}
		while (resultWhile <= 10) {
			System.out.print(resultWhile);
			resultWhile++;
		}

	}

}
